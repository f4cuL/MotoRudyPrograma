    private void tablaClientesRemitoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaClientesRemitoMouseClicked
       int fila= tablaClientesRemito.getSelectedRow();
       if (fila==-1){
           JOptionPane.showMessageDialog(null,"Usuario no seleccionado");
       }else{
           int id= Integer.parseInt(tablaClientesRemito.getValueAt(fila,0).toString());
           String nombre= (String)tablaClientesRemito.getValueAt(fila,1);
           String apellido= (String)tablaClientesRemito.getValueAt(fila,2);
           int dni= Integer.parseInt(tablaClientesRemito.getValueAt(fila,3).toString());
           Cliente cli = new Cliente(id,nombre,apellido,dni);
           controlador.getModelo().setClienteAux(cli);
           controlador.getModelo().listarRemitos(tablaRemitos, cli);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_tablaClientesRemitoMouseClicked

